package weeks;

public class ObjectManipulationClass {

	private String name;
	private int age;
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
		
	}
	public int getAge()
	{
		return age;
		
	}
	public ObjectManipulationClass(String name, int age) 
	{
		this.name = name;
		this.age = age;
		
	}
	
	
}
